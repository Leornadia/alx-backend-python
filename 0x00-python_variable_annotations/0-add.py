#!/usr/bin/env python3
"""
This module contains a function to add two float numbers.
"""

def add(a: float, b: float) -> float:
    """
    Adds two float numbers together.

    Args:
        a (float): The first number.
        b (float): The second number.

    Returns:
        float: The sum of the two numbers.
    """
    return a + b

